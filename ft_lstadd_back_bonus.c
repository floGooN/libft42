#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (!new)
		return ;
	temp = ft_lstlast(*lst);
	if (!temp)
	{
		*lst = new;
		new->next = NULL;
	}
	else
		temp->next = new;
}
